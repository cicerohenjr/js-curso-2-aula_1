// Desafio 2: Função que recebe um nome e exibe "Olá, [nome]!" no console
function exibirSaudacao(nome) {
	console.log(`Olá, ${nome}!`);
}

// Desafio 3: Função que recebe um número e retorna o dobro
function dobro(numero) {
	return numero * 2;
}

// Desafio 4: Função que recebe três números e retorna a média
function media(a, b, c) {
	return (a + b + c) / 3;
}

// Desafio 5: Função que recebe dois números e retorna o maior
function maior(a, b) {
	return a > b ? a : b;
}

// Desafio 6: Função que recebe um número e retorna sua multiplicação por ele mesmo (quadrado)
function quadrado(numero) {
	return numero * numero;
}

